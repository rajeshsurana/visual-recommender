<ol>
<li>The primary research question we had in mind was whether we can build a prototype that can make a recommender system that works well in any kind of
screen-sized devices. This led us to the development of a responsive visual recommender that is not only mobile adaptive but also adaptive to desktops and laptops. (Key fact: 25%  of Internet users only access the internet via mobile devices in the United States. For details see attached report.)</li>
<li>Providing a single window to display both personalized and generalized recommendations for quick comparison across various attributes.</li>
<li>Using visualization instead of numbers to convey ranking. We used Heat Map to suggest ranking.</li>
<li>Eliminating need of big screen devices to access stackoverflow like Question and Answer websites while on the go.</li>
<li>Developing a prototype with reasonably transparent system logic to induce user's trust in our system.</li>
